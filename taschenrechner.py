zahl_1 = int(input("Gib die erste Zahl ein: "))
operator = input("Gib den Operator ein (+, -, *, /): ")
zahl_2 = int(input("Gib die zweite Zahl ein: "))

if operator == "+":
    ergebnis = zahl_1 + zahl_2
    print(f"Das Ergebnis von {zahl_1} + {zahl_2} ist {ergebnis}")
elif operator == "-":
    ergebnis = zahl_1 - zahl_2
    print(f"Das Ergebnis von {zahl_1} - {zahl_2} ist {ergebnis}")
elif operator == "*":
    ergebnis = zahl_1 * zahl_2
    print(f"Das Ergebnis von {zahl_1} * {zahl_2} ist {ergebnis}")
elif operator == "/":
    ergebnis = zahl_1 / zahl_2
    print(f"Das Ergebnis von {zahl_1} / {zahl_2} ist {ergebnis}")

elif operator == "%":
    ergebnis = zahl_1 % zahl_2
    print(f"Das Ergebnis von {zahl_1} % {zahl_2} ist {ergebnis}")
elif operator == "a":
    print("Hallo")

